async getTudo() {
  const promessas = [
    this.getVestidos(), this.getBlusas(), this.getCalcas(),
    this.getCasacos(), this.getSaias(), this.getCamisas(),
    this.getMacacos(), this.getCroppeds(), this.getBodys(),
    this.getBlazers(), this.getShorts(), this.getAcessorios()
  ];

  const categorias = await Promise.all(promessas.filter(p => p !== undefined));
  let todos = categorias.flat();

  // ALGORITMO DE EMBARALHAMENTO (Fisher-Yates)
  for (let i = todos.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [todos[i], todos[j]] = [todos[j], todos[i]]; // Inverte as posições
  }

  return todos;
}




